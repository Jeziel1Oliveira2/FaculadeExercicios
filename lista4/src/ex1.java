public class ex1 {
    public static void main(String[] args) {
        //criando o vetor com 10 elementos
        int [] vetorX = new int[10];
        //colocando o valor 30 nos 10 elementos do vetor e imprimindo eles
        for (int i = 0; i < vetorX.length; i++) {
            vetorX[i] = 30;
            System.out.println(vetorX[i]);
        }
    }
}
